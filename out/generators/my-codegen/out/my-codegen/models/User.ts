
/**
 * @interface User
 */
export interface User {

    /**
     * 
     * @type {number}
     * @memberof User
     */
    id?: number;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    username?: String;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    firstName?: String;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    lastName?: String;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    email?: String;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    password?: String;
    /**
     * 
     * @type {String}
     * @memberof User
     */
    phone?: String;
    /**
     * 
     * @type {number}
     * @memberof User
     */
    userStatus?: number;
}



